public class AreaOfRecTriCir {
    public static void main(String[] args) {
        int l = 10;
        int w= 20;
        int h=20;
        int b=30;
        float r = 5;
        System.out.println("Area of Rectangle: "+ l*w);
        System.out.println("Area of Triangle: "+ 0.5*b*h);
        System.out.println("Area of Circle: "+ Math.PI*r*r);
    }
}
